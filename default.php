<h1 class="display-4">Expo Plaza</h1>
<p class="lead">Das Messegelände Hannover befindet sich im Süden der Landeshauptstadt Hannover (Stadtteil Mittelfeld) und ist mit einer überdachten Fläche von 496.000 m² und 26 Messehallen das größte Messegelände der Welt. Zu der 131 Hektar großen Kernfläche gehört außerdem ein Tagungszentrum. Die Betreiberin des Geländes ist die Deutsche Messe AG (DMAG), die auch Messen außerhalb des Geländes veranstaltet. Im Jahr 2000 wurde auf dem gesamten Messegelände die Expo 2000 ausgerichtet. Auf dem Messegelände werden jährlich bedeutende Messen wie die Agritechnica oder die Hannover Messe ausgerichtet.</p>
<p class="lead">Der Expo Plaza selbst umfässt lediglich die TUI-Arena, den Deutschen Pavillion sowie die MMBBS und die Fakultät 3 der Fachhochschule Hannover. Er war ein Teil der Expo 2000 zu deren Anlass auch viele weitere Pavillions errichtet wurden. Hier sind einige Fotos der Pavillions aus der Zeit der Expo 2000.</p>
