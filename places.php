<h1 class="display-4">Orte</h1>
<p>Hier eine kleine Übersicht zu den Orten auf dem Expo Plaza. Der Expo Plaza ist ein großer Platz mit reichlich grüner Beflanzung und einer Vielzahl verschiedener Gebäude mit diversen achitektonischen Stilen. Einige davon sind Bildungseinrichtungen, andere werden weiterhin für Ausstellungen verwendet.</p>
<div class="col-md-12">
	<div class="row">
		<div class="col-md-8">
			<p>Die MMBbS ist ein regionales Kompetenzzentrum für IT- und Medienberufe in einer sich weiterentwickelnden Berufs- und Arbeitswelt. In ihr werden fächerübergreifend technische, soziale und gestalterische Kompetenzen in Zusammenarbeit mit den Betrieben vermittelt. Sie ist ein Zentrum für Erst- und Weiterbildung, das nachhaltige Bildungsprozesse initiiert und sicherstellt. Die MMBbS ist gleichberechtigte Partnerin mit Schülern, Eltern und Wirtschaft.</p>
